    <h2><a id="conteudo"><img src="imagens/MiniLogoV1_50.png" alt="logo de um tiranosauro rex verde usando extensores vermelhos e amarelos para seus pequenos braços"> Braile</a></h2>
    <hr/>
    <p>A primeira escola de ensino para jovens cegos foi fundada em 1784 na frança por Valentim Hauy, e se chamava Institut Royal des Jeunes Aveugles (Institito Real dos Jovens Cegos), porém não existiam sistemas de leitura em Braile, eles utilizavam caracteres com linhas em alto relevo.</p>
    <p>Braile foi inventado somente em 1925 por Louis Braile, também na frança.</p>
    <p>Após isto, varias escolas especializadas  começaram a surgir na europa, como a inglesa "St. Dunstan" por exemplo, que tinha ênfase também aos aspectos psicologicos dos alunos, após isto passou-se a implantação de salas especiais em escolas publicas.</p>
    <p>Luis Braile estudou na instituição fundada por Valentim Hauy.</p>
    <p>Em 1819 mesmo ano em que Braile iniciou na instituição, o capitão de artilharia Carlos Barbier de la Serre, passa a se interessar pela escrita dos cegos, o Captão , sentiu necessidade de transmitir mensagens em obscuridade e designou coordenadas com seus simbolos sonográficos com base em numeros de pontos, com indicativos de linha e coluna, colocados em duas filas verticais e paralelas, que foi batizada como "escrita Nocturna", sendo estudada e adaptada posteriormente por Louis Braile.</p>
