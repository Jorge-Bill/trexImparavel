    <h2><a id="conteudo"><img src="imagens/MiniLogoV1_50.png" alt="logo de um tiranosauro rex verde usando extensores vermelhos e amarelos para seus pequenos braços"> Mercado de Trabalho</a></h2>
    <hr/>
    <img src="imagens/trab.png" alt="Representação de deficiencias humanas" class="pull-right">	
    <p>O Programa de Promoção do Acesso das Pessoas com Deficiência Beneficiárias do Benefício de Prestação Continuada da Assistência Social à Qualificação Profissional e ao Mundo do Trabalho - Programa BPC Trabalho, foi instituído pela Portaria Interministerial nº 2, de 02 de agosto de 2012. É uma iniciativa do Governo Federal, a ser realizada pelos Ministérios do Desenvolvimento Social e Combate à Fome (MDS), da Educação (MEC), do Trabalho e Emprego (MTE) e da Secretaria de Direitos Humanos da Presidência da República (SDH/PR), e envOBJETIVO.</p>
    <p>Seu objetivo é promover o protagonismo e a participação social dos beneficiários com deficiência do Benefício de Prestação Continuada da Assistência Social – BPC, por meio da superação de barreiras, fortalecimento da autonomia, acesso à rede socioassistencial e de outras políticas, à qualificação profissional e ao mundo do trabalho, priorizando a faixa etária de 16 a 45 anos.</p> 
    <p>As principais ações do Programa BPC Trabalho no âmbito municipal e no Distrito Federal são: identificação e busca ativa dos beneficiários do BPC com deficiência, na faixa etária de 16 a 45 anos, realizada pelos técnicos dos Centros de Referência da Assistência Social – CRAS; realização de diagnóstico social e avaliação em relação ao interesse e possibilidade de participação no Programa; acompanhamento das pessoas com deficiência beneficiárias do BPC e de suas famílias, com a finalidade de garantir oferta de serviços e benefícios socioassistenciais e de encaminhamento para as demais políticas públicas.</p>
    <p>De acordo com a Lei Orgânica da Assistência Social - LOAS (Lei nº 8.742, de 07/12/1993 alterada pelas Leis nº 12.435 de 06/07/2011 e nº 12.470, de 31/08/2011), as pessoas com deficiência, beneficiárias do BPC, que entram no mercado de trabalho, terão o pagamento do seu benefício suspenso, em caráter especial. Mas, se perderem o emprego, poderão voltar a receber o BPC, sem precisar solicitar novo benefício, nem passar pela perícia médica e pelo serviço social do INSS. Durante o período em que a pessoa com deficiência estiver exercendo atividade remunerada o benefício ficará suspenso e poderá ter sua continuidade requerida se a relação trabalhista for extinta.</p>
    <p>O registro do beneficiário do BPC e sua família no Cadastro Único para Programas Sociais do Governo Federal é muito importante, pois o cadastro é a porta de entrada para o recebimento dos benefícios dos programas sociais, como o Bolsa Família, Minha Casa Minha Vida, Telefone Popular, Tarifa Social de Energia Elétrica, Bolsa Verde, dentre outros.</p> 
