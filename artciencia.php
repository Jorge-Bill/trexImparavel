    <h1>Campo da Ciência</h1>
          <p>Atualmente a tecnologia caminha em passos largos, ela é ultilizada em todos as áreas de pesquisa e em uma dessas áreas que nós abordaremos, que será a acessibilidade de pessoas com deficiências.</p>
          <p>Hoje já estamos ultilizando a tecnologia no dia a dia, para orientação, produção e sobrevivência. Já é uma realidade no campo da medicina usarem partes róboticas para substituir partes dos nossos orgão, na qual essas partes comportam-se igual ou melhor que os orgãos originais.</p>
          <p>Um caso em que a tecnologia ajudou muito, foi a do fisico Stephen Hawking um dos maiores fisicos que já existiram ele sofre de uma doença chamada esclerose lateral amiotrófica que de tempos em tempos paralisa todos os musculos do corpo. para conseguir se locomover, se comunicar e sobreviver. A tecnologia tornou isso possivel com sintetizador de voz e cadeira de rodas que controlada pelos musulos da face, com um computador acoplado.</p>
          <p>Assim podemos perceber o qão importante é a tecnologia que usamos. Más para inclusao digital estámos muito longe de ter adaptação para quem é deficiênte com a nova onda de celulares com "touch screen" pessoas com deficiência visual não conseguem ter uma boa interatividade e na maioria das vezes não conseguem nem interagir com os gadgets. As empresas tentão construir produtos acessiveis, mas eles não contém as mesmas funcionalidades que os outros, e com isso bem menos interagivel que os normais. </p>
          <p>Pensando nisso organizações de pesqueisas com as universidades estão trabalhando para produzirem novos equipamentos para melhorar a acessibilidade. Como o caso da mecDaisy produzido pela A Universidade Federal do Rio de janeiro
(UFRJ) em conjunto com o minitério da educação, nomeado assim por ser baseado no padrão
internacional Daisy (sigla em inglês para Sistema de Informação de Acesso
Digital). O programa permite a transformação de textos escritos para a linguagem
oral, digitalmente
através de um sintetizador de voz. Ainda
Nossa Linha Acessibilidade.
oferece a adaptação em caracteres ampliados, opção de impressão em
Braille e descrição de figuras, gráficos e imagens presentes no texto.</p>
          <p>Já pesquisadores da Universidade Estadual Paulista (Unesp) construíram o
protótipo de um aparelho capaz de traduzir documentos e páginas da
Internet diretamente para o alfabeto Braille, descartando a necessidade de
impressoras especiais. A superfície do chamado dispositivo anagliptográfico exibe os sinais em alto relevo e funciona como uma
espécie de teclado de leitura conectado com computadores e notebooks.</p>
          
<p>Assim a tecnologia na acessibilidade está caminhando para igualdade de todos e que todas as pessoas tenham acesso a informação.</p>
