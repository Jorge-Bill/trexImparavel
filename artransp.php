    <img src="imagens/transp.png" alt="Logo da SP-Trans" class="pull-left">
			<p> O Serviço de Atendimento Especial - ATENDE foi criado por meio do decreto nº 36.071 de 09 de maio de 1996. É uma modalidade de transporte porta a porta, gratuito aos seus usuários, com regulamento próprio, oferecido pela Prefeitura do Município de São Paulo, gerenciado pela São Paulo Transporte S.A. e operado pelas empresas de transporte coletivo do município de São Paulo.</p>

    <p>Destina-se às pessoas com deficiência física com alto grau de severidade e dependência, no horário das 7h às 20h, de segunda-feira a domingo.</p>

    <p>O atendimento é prestado a clientes cadastrados, com uma programação pré-agendada de viagens.</p>

    <p>Além do atendimento porta a porta a clientes cadastrados, o Atende oferece atendimentos nos fins de semana, denominados de "eventos aos fins de semana". Neste tipo de serviço, os pedidos de transporte são feitos diretamente pelas instituições que trabalham com pessoas com deficiência (com, no mínimo, uma semana de antecedência). As instituições precisam efetuar cadastro prévio na SPTrans.</p>

    <p>O Atende tem veículos devidamente adaptados e roda cerca de um milhão de quilômetros/mês.</p>

    <p>Todos os usuários do Serviço de Atendimento Especial - ATENDE e demais munícipes interessados em obter informações, podem fazê-lo por meio do telefone 0800-155 234 (de 2ª a 6ª feira, das 7 às 19 horas) e pela Central de Atendimento 156 da Prefeitura ou ainda nos postos de atendimento das Subprefeituras.</p>
 
