    <h2><a id="conteudo"><img src="imagens/MiniLogoV1_50.png" alt="logo de um tiranosauro rex verde usando extensores vermelhos e amarelos para seus pequenos braços"> Sinais</a></h2>
    <hr/>
    <p>A linguagem exerce papel preponderante na constituição dos sujeitos, visto que é através dela que nos apropriamos da cultura entorno, construímos nosso entendimento sobre o micro e macro universo, e estabelecemos nossas relações sócio-afetivas. Apesar da relevância inquestionável da sua importância para os seres humanos, a possibilidade de construção da mesma para os surdos, tem sido secularmente negada, ao privá-los da aprendizagem da Língua de Sinais desde tenra idade. Esta resistência à Libras, presente tanto no seio familiar das crianças surdas, como na maioria das instituições de ensino, deriva dos mitos e preconceitos que se autoperpetuam em relação aos surdos e a Língua de Sinais. Contudo os estudos realizados com crianças surdas filhas de pais surdos, as quais têm a possibilidade de aprenderem a Língua de Sinais como primeira língua, já nos primeiros contatos com o seu entorno, são conclusivos no sentido de mostrar a importância da mesma para que estas construam uma linguagem plena e autêntica, alcançando os mesmos patamares de desenvolvimento do que as crianças ouvintes inseridas em um ambiente linguístico de modalidade oral-auditiva.</p>

    <p>A linguagem é indubitavelmente fundamental para os sujeitos, na medida em que é através dela que nos apropriamos da cultura do entorno, construímos nosso entendimento sobre o micro e macro universo e estabelecemos nossas relações socioafetivas. Desta forma, entendemos que a linguagem exerce papel preponderante na constituição dos sujeitos, de modo que “é nela, por ela e com ela que (...) nos tornamos ‘humanos’” (LACERDA, 1998, p. 38-39).</p>

    <p>Apesar da relevância inquestionável da importância da linguagem para os seres humanos, a possibilidade de construção da mesma para os surdos tem sido secularmente negada. A ideologia oralista impõe, desde o Congresso de Milão de 1880, quando se proibiu o uso da Língua de Sinais, banindo-a das escolas, a oralização dos surdos. Sendo que, atualmente, apesar do amparo legal da nova legislação, que reconhece Libras como a língua natural das pessoas surdas brasileiras, na maioria dos espaços escolares ainda continua sendo negligenciada.</p>

    <p>Verifi ca-se que a Língua de Sinais é utilizada muito mais como um instrumento a fim de ensinar a Língua Portuguesa, que possui prestígio e goza de reconhecimento social dentre os professores que, em sua grande maioria, não possuem fl uência em Libras e, tampouco, qualifi cação metodológica voltada ao ensino de alunos surdos. Em decorrência deste contexto, perde-se a expectativa de excelência que deveria pautar a proposta bilíngue de ensino, também prevista em lei, para os sujeitos surdos.</p>

    <p>Os mitos e os preconceitos que se perpetuam em relação aos surdos e à Língua de Sinais legitimam práticas de oralização, nas quais predominam o treinamento da leitura labial e a terapia da fala, com o objetivo precípuo de dissimular a surdez, buscando transformar os surdos em pseudoouvintes, conferindo-lhes um lugar de não-eu.</p>

    <p>Neste processo, o uso da ciência e da tecnologia se constitui na promessa redentora de corrigir a defi ciência através dos melhoramentos protéticos e implantes cocleares, transformando os surdos nos ciborgues da surdez (LOPES, 2004).</p>

    <p>Enquanto todos os investimentos continuam sendo destinados predominantemente à “correção da surdez”, os surdos, ao serem privados da Língua de Sinais, são concomitantemente castrados da possibilidade de construírem uma linguagem plena e autêntica, desenvolvendo suas capacidades cognitivas, linguísticas, sociais e emocionais correlatas. Segundo Perlin (2004, p. 79), esta “condescendência a automutilação ou ciborguização dos surdos” constitui-se em uma violência, provocando o “surdicídio” destes sujeitos ao impor-lhes um modelo de identidade ouvinte que continua vigorando.</p>

    <p>O objetivo do presente ensaio é mostrar a importância da Língua de Sinais para os surdos, como constituindo sua língua natural, fundamental para a construção da sua linguagem-identitária, cuja função seja a da promoção de todos os desenvolvimentos individuais e sociais vinculados a mesma; da mesma maneira como a língua de modalidade oral-auditiva possibilita aos ouvintes seu desenvolvimento. Nesta discussão pretende-se, portanto, focalizar alguns dos mitos e preconceitos que ainda imperam em relação à Língua de Sinais, cuja consequência nefasta implica o afastamento dos surdos da possibilidade de construírem uma linguagem, na sua língua de modalidade visuoespacial, ainda que distinta daquela instituída pelos ouvintes como a do padrão de normalidade.</p>
