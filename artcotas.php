    <p>Baseada nesse contexto, que a presidenta da república sancionou a Lei nº12.711, a Lei de Cotas, que dispõe sobre a reserva de 25% das vagas em instituições e universidades federais para alunos que tenham cursado integralmente o ensino médio na rede pública de ensino. Desse percentual, 50% é destinado a estudantes de famílias com renda igual ou inferior a um salário-mínimo e meio per capita. Critérios de cor e raça também são levados em consideração.</p>

    <p>Os programas do governo que favorecem a entrada de alunos de baixa renda e egressos de escola pública, são: Enem (Exame Nacional de Ensino Médio), Prouni (Programa Universidade para Todos) - que também reserva vagas para alunos com deficiência - e Sisu (Sistema de Seleção Unificada).</p>
