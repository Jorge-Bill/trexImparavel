<h2>Cotas em Concurso Público para Pessoas com Deficiência</h2>
<hr/>

    <p>As cotas para pesssoas com deficiência ou pessoas com necessidades especiais em concurso público é uma eterna pauta de discussões e alvo de polêmica entre os concurseiros e órgãos públicos. De um modo geral, a polêmica gira em torno do que é ou não considerado deficiência e dos direitos à vaga da pessoa com deficiência, em relação, principalmente, à classificação do candidato no concurso.</p>

    <p>O Decreto nº 3.298, que delibera sobre a Política Nacional para a Integração da Pessoa Portadora de Deficiência, dispõe em Seu Art. 3º e 4º sobre o que é considerado deficiência, deficiência permanente e incapacidade e em quais categorias a pessoa com deficiência pode se enquadrar. Entre outros artigos, também dispõe sobre a reserva de vagas no concurso público:</p>

    <p>Segundo o Decreto Nº 3.298/1999:

    Art. 37. Fica assegurado à pessoa portadora de deficiência o direito de se inscrever em concurso público, em igualdade de condições com os demais candidatos, para provimento de cargo cujas atribuições sejam compatíveis com a deficiência de que é portador.

    § 1o O candidato portador de deficiência, em razão da necessária igualdade de condições, concorrerá a todas as vagas, sendo reservado no mínimo o percentual de cinco por cento em face da classificação obtida.

    Art. 39. Os editais de concursos públicos deverão conter:

    I - o número de vagas existentes, bem como o total correspondente à reserva destinada à pessoa portadora de deficiência;

    II - as atribuições e tarefas essenciais dos cargos;

    III - previsão de adaptação das provas, do curso de formação e do estágio probatório, conforme a deficiência do candidato; e

    IV - exigência de apresentação, pelo candidato portador de deficiência, no ato da inscrição, de laudo médico atestando a espécie e o grau ou nível da deficiência, com expressa referência ao código correspondente da Classificação Internacional de Doença - CID, bem como a provável causa da deficiência.

    Art. 41. A pessoa portadora de deficiência, resguardadas as condições especiais previstas neste Decreto, participará de concurso em igualdade de condições com os demais candidatos no que concerne:

    I - ao conteúdo das provas;

    II - à avaliação e aos critérios de aprovação;

    III - ao horário e ao local de aplicação das provas; e

    IV - à nota mínima exigida para todos os demais candidatos.

    A lei reserva um percentual de vagas para deficientes, exceto quando o cargo ou emprego público exija aptidão plena do candidato.

    O inciso 8, do Artigo 37 da Constituição Federal diz que:

    VIII - a lei reservará percentual dos cargos e empregos públicos para as pessoas portadoras de deficiência e definirá os critérios de sua admissão;</p>
        
