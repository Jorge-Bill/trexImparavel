    <hr/>
    <p>Segundo a Organização Nacional das Nações Unidas (ONU), 10% da população mundial tem alguma deficiência. A Lei 8.213/91 chamada de Lei de Cotas para deficientes, determina uma cota mínima para pessoas com alguma deficiência em empresas com mais de 100 empregados. Isso trouxe benefícios para muitos deficientes que foram colocados no mercado de trabalho. A proporção de vagas se dá na seguinte forma: de 100 a 200 empregados com 2% de deficientes no quadro de funcionários, de 201 a 500, com 3%; de 501 a 1000, com 4% e acima de 1001, com 5%. As empresas que não cumprem a demanda estão sujeitas à multas. Para essa Lei, a deficiência é considerada quando ocorre a perda ou anormalidade da estrutura ou de sua função psicológica ou fisiológica.</p>

    <p>A fiscalização das cotas só teve início em 2001. Há muitas vagas oferecidas pelas empresas para os deficientes; entretanto, a falta de capacitação de grande parte dos candidatos é uma das maiores dificuldades enfrentadas pela empresas. Uma outra dificuldade encontrada é o fato de que a empresa só pode dispensar um empregado caso tenha outro para substituí-lo. Além da contratação, a empresa deve adaptar seu espaço físico para receber os deficientes e também deve ser feita a conscientização dos funcionários da importância da inclusão e a responsabilidade social da empresa.</p>

    <p>Com a contratação dos deficientes, a vantagem é mútua. As empresas crescem em inclusão social e ocorre a diminuição do desemprego por parte dos deficientes. Além disso, as empresas ganham em credibilidade social. As pessoas pensam que contratar deficientes implica em realizar grandes adaptações na empresa, mas nem toda deficiência exige essas alterações. As áreas que mais contratam deficientes são as que atendem a área de administração das corporações. Uma forma de qualificar os deficientes é procurar associações para pessoas com deficiência. Eles oferecem apoio e qualificação, além de indicações para o mercado de trabalho.</p>

    <p>Uma outra alternativa é procurar incentivos do Estado para que ensinem um ofício para as pessoas portadoras de necessidades especiais.</p>

    <p>Em outros países, o incentivo para pessoas com deficiência envolve diversos fatores como incentivos fiscais e formação profissional com contribuições dos empresários. Como funcionam em alguns países:</p>

    <ul>
        <li>Portugal: A cota é de 2% de trabalhadores com deficiência para empresas privadas e 5% para empresas públicas.</li>
        <li>Espanha: 2% de vagas em empresas com mais de 50 trabalhadores fixos e a Lei n° 63/97 assegura incentivos fiscais.</li>
        <li>França: O Código do Trabalho Francês estabelece 6% das vagas de trabalho em empresas com mais de 20 empregados.</li>
        <li>Itália: As empresas públicas e privadas com mais de 50 empregados devem contratar 7% de pessoas com deficiência para seu quadro de funcionários. Nas empresas que empregam de 36 a 50 funcionários, é estabelecida a reserva para duas pessoas com deficiência e nas que mantêm de 15 a 35 funcionário, devem manter 1 funcionário com deficiência.</li>
        <li>Alemanha: Para empresas com mais de 16 funcionários devem ser reservadas 6% de vagas e há um incentivo para que os empresários contribuam para medidas de profissionalização de pessoas com deficiência.</li>
        <li>Áustria: 4% das vagas são destinadas aos deficientes nas empresas com mais de 25 empregados.</li>
        <li>Argentina: Incentivos fiscais para empresas privadas que contratem a demanda e a reserva de 4% para contratar servidores públicos.</li>
    </ul>
