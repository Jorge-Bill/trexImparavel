<h2>Cotas para estudantes de escola pública e baixa renda</h2>
<hr/>
    <p>Em 2010, a Universidade Federal do Rio de Janeiro decidiu destinar 20% de suas vagas para alunos egressos das escolas públicas municipais e estaduais. Apesar disso, a medida não inclui os estudantes negros e índios. É a oportunidade dos alunos da rede pública cursarem o Ensino Superior, pois eles têm que concorrer com alunos que vêm de escola particular. Isso torna, segundo especialistas, o direito à educação mais igualitário, pois as escolas públicas são sucateadas e não oferecem a estrutura e o conhecimento necessários para o ingresso na Faculdade.</p>

    <p>As vagas para estudantes de escola pública são vistas como a melhor forma de política de cotas (cotas sociais), uma vez que os argumentos de quem discorda da medida são de que o problema brasileiro é social e não racial. Dessa forma, iria abranger todas as raças e beneficiar aqueles que realmente não têm condições de concorrer no vestibular com alunos vindos de escolas particulares.</p>

