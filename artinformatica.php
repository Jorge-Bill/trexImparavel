    <h1>Acessibilidade na infórmatica</h1>
          	<p>A expressão acessibilidade Web refere-se a prática inclusiva de fazer websites que possam ser utilizados por todas as pessoas que tenham deficiência ou não. Quando os sites são corretamente concebidos, desenvolvidos e editados, todos os usuários podem ter igual acesso à informação e funcionalidade.</p>

<p>Por exemplo, quando um site é desenvolvido utilizando corretamente as semânticas HTML5, acrescentando a descrição textual alternativa equivalente ao conteúdo das imagens e com links nomeados de forma significativa, esta ação ajuda usuários cegos ou com deficiência visual a obterem auxílio com o uso de softwares Leitores de Textos.</p>
 <h3>Algumas tecnologias desenvolvidas para pessoas com deficiência</h3>
          
          <h3>Leitores de tela</h3>
          
    <p>Para que os deficientes visuais tenham acesso as informçãoes digitais, é necessario utilizar alguma ferramenta, essas ferramentas são os leitores de tela ao qual iram ler as informções que estão na tela e o descreverão com sons e falas para o usuario deficiênte. Abaixo estão os leitores de tela mais ultilizados para cada plataforma.</p>
          
          
    <h4>Jaws</h4>
          
       <p> JAWS (acrônimo para Job Access With Speech) é um leitor de tela ex, um programa de computador projetado para a plataforma windows para usuários com deficiência visual, produzido pelo Blind and Low Vision Group da empresa Freedom Scientific, de Saint Petersburg, Flórida, Estados Unidos. Pode ser usado em diversos idiomas inclusive Português do Brasil.</p>
          
     <h4>Orca</h4>
          <p>o Orca é um leitor de tela produzido para linux pela organização Sun Microsystems, Inc. Com auxilio de muitos membros da comunidade para a melhoria. Ele já está incluso nas distribuições que trabalham com GNOME 2.16.O Orca é projetado para trabalhar com aplicações e "toolkits" que suportam a interface provedora de serviço em tecnologia assistiva (AT-SPI). Isso inclui o GNOME Desktop e suas aplicações, OpenOffice, Firefox, e a plataforma Java. </p>
          
          
    <h4>ChromeVox</h4>
          
          <p>O Chromevox é uma extensão para o navegador chrome  que integra  um leitor de tela no navegador, sem a necessidade de usar o leitor de tela  instalado no sistema. Para usuários com visão subnormal, essa extensão melhora  a experiência com a web proporcionando uma navegação menos cansativa. Essa extensão atende a todas pessoas com alguma deficiência visual.Ela é projetada para ser uma ferramenta web. </p>
    
    <h3>Link direto para o conteúdo</h3>
          <p>Para que um deficiêntetenha fluidez na navegação e possa ter acesso mais rápido a informação, coloca-se um link diretamente para o conteúdo, para o úsuario que irá usar um leitor de tela, possa navegar diretamente para o conteúdo, e não prescise navegar todos os itens até chegar ao conteúdo.</p>
          
          <h3>Link para o topo</h3>
          
           <p>Assim como o link para o conteúdo ele será um atalho para facilitar o usuário, mas isso não só ajuda pessoas com deficiência e sim todas as pessoas em geral, assim sempre que vc estiver longe do topo da página poderá voltar com um click</p>
              
          <h3>Recurso Alt nas imagens</h3>
        <h4>Alt nas imagens</h4>
        <p>O recurso alt tem a função de descrever a imagem, para que o leitor de tela consiga descrever a imagem, segundo escrito no alt.</p>
        
        <h4>Alt silêncioso</h4>
    <p>É o recurso usado quando não quer que seja descrita a imagem assim o leitor de tela passará sem a descrever, normalmente usado para recursos gráficos de design, é ultilizado o alt="...".</p>
        
          <h3>Galeria de imagens acessível</h3>
        <p>Para que as imgens fiquem acessiveis e facilite a navegação e a compreenssão dos deficientes. Assim é feita uma lista não ordenada para que se organize as imagens e após é organizada com os recursos css.</p>
          
          <h3>Recurso Cite</h3>
        <p>É a tag do html5 usada para citar o titulo de algo citando quem o produziu e linguagem entre outras caracteristicas do trabalho.  </p>
    <h3>Recurso caption</h3>
        <p>Da ao texto uma legenda ou um titulo que é normalmente usado em uma tabela </p>
          
           <h3>Tabela acessivel</h3>
    
    <p>Para deixar a tabela acessível é necessário dar um titulo a ela com o recurso caption e usar o recurso scope da tag th para informar o que está descrito nas linhas (row) e nas colunas (col) </p>
        
          <table>
            <caption>Tabela de preços das pizzas</caption>
            <tr>
                <th scope="col">Sabor</th>
                <th scope="col">Tamanho</th>
                <th scope="col">Preço</th>
