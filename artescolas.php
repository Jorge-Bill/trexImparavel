    <h2><a id="conteudo"><img src="imagens/MiniLogoV1_50.png" alt="logo de um tiranosauro rex verde usando extensores vermelhos e amarelos para seus pequenos braços">Escola Acessivel</a></h2>
    <hr/>
    <p>O Programa Escola Acessível disponibiliza recursos, por meio do Programa Dinheiro Direto na Escola - PDDE, às escolas contempladas pelo Programa Implantação de Salas de Recursos Multifuncionais. No âmbito deste programa são financiáveis as seguintes ações:</p>

    <p>Adequação arquitetônica: rampas, sanitários, vias de acesso, instalação de corrimão e de sinalização visual, tátil e sonora;</p>

    <p>Aquisição de cadeiras de rodas, recursos de tecnologia assistiva, bebedouros e mobiliários acessíveis;</p>

    <p>Como acessar: As escolas contempladas, conforme relação anual publicada em Resolução FNDE/PDDE – Escola Acessível, efetivam cadastro no Sistema Integrado de Monitoramento, Execução e Controle do Ministério da Educação - SIMEC, onde inserem o plano de atendimento contendo o planejamento de utilização dos recursos.</p>

    <p>As escolas contempladas pelo Programa Escola Acessível 2014 já podem acessar SIMEC – Sistema Integrado de Monitoramento Execução e Controle do Ministério da Educação e elaborar seu Plano de Atendimento.</p>
    
