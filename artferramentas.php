    <h1>Ferramentas Acessíveis</h1>
          <p>Algumas empresas produzem equipamentos tecnologicos visando melhorar a vida de pessoas com deficiência. Com isso aumentar a acessibilidade para todas as pessoas independente da deficiência. Abaixo algumas dessas ferramentas tecnológicas. </p>
          
          
          <h3>Bengala luminosa</h3>

<p></p>É um projeto recente, desenvolvido por uma empresa brasileira, que auxilia idosos e pessoas com mobilidade reduzida a se locomover em ambientes com pouca ou nenhuma luminosidade. O produto já está disponível no mercado brasileiro</p>

<p>Como funciona - A bengala pesa um pouco mais de meio quilo e mede, aproximadamente, 80 cm. A luminária é alimentada por pilhas e pode ser acionada por meio de um botão na parte superior</p>

<p>Preço - R$ 70 Fabricante - Cajumoro</p>

              <h3>Pernas robóticas</h3>

              <p>O protótipo do Rex (exoesqueleto robótico, em inglês) foi desenvolvido para ajudar pessoas com paraplegia a se locomover. Com as pernas robóticas, elas podem andar e até subir escadas. A tecnologia já é comercializada na Nova Zelândia, mas só estará disponível no mercado mundial a partir de 2011</p>

              <p>Como funciona - As pernas são controladas por um joystick e os movimentos são alimentados por uma bateria recarregável, que dura cerca de duas horas. A roupa pesa 38 kg e pode ser usada diariamente</p>

              <p>Preço - US$ 150 mil Fabricante - Rex Bionics</p>

              <h3>Mouse ócular</h3>

              <p>Utiliza a movimentação dos músculos do globo ocular para controlar o cursor do mouse. O sistema, voltado principalmente para tetraplégicos, diferencia sinais voluntários e involuntários, evitando que comandos errados sejam processados. O Fabricante - aguarda patrocínio para produzi-lo em larga escala</p>

<p>Preço - Produto ainda não disponível no mercado Fabricante - Fundação Paulo Feitoza</p>

<p>1. Com eletrodos fixados na face, em volta dos globos oculares, a pessoa controla o cursor do mouse mexendo os olhos e piscando</p>

<p>2. Um transdutor eletrônico processa os sinais captados pelos eletrodos e os converte em comandos elétricos</p>

<p>3. O computador recebe os comandos do transdutor eletrônico e executa os movimentos do cursor</p>

<p>4. O cursor é controlado pelo movimento dos músculos oculares. Então, basta mover os olhos e piscar para clicar</p>

              <h3>Teclado virtual</h3>

<p>É um software que permite que o usuário digite textos no computador. O programa possui correção ortográfica e teclas com textos prontos ("estou com calor" ou "estou com sede", por exemplo). Também tem a opção "Voz", um sintetizador que "fala" o texto digitado, permitindo comunicação verbal</p>

<h3>Mouse visual</h3>

    <p>O software permite que o usuário controle o cursor do mouse apenas com movimentos de cabeça, captados por uma webcam. O programa identifica o ponto central, que fica entre os olhos e um pouco acima do nariz, para iniciar o processo</p>

<p>Preço - Não divulgado</p>

<p>Fabricante - Fundação Paulo Feitoza</p>

<h3>Guincho de piscina</h3>

    <p>Equipamento projetado para facilitar o transporte de pessoas com deficiência física para a piscina. O aparelho pode ser fixado na parede ou no chão e ocupa pouco espaço</p>

    <p>Como funciona - A cadeira, que é submersível, é acionada por meio de um sistema elétrico. O guincho, que tem capacidade para suportar no máximo 150 kg, é produzido em aço inoxidável e pode ser desmontado</p>

    <p>Preço - R$ 12 mil</p>

    <p>Fabricante - Cajumoro</p>

              <h3>Stand table</h3>

    <p>É um aparelho que permite que o usuário portador de deficiência física se locomova na posição ereta (de pé), saindo da cadeira de rodas. Nessa posição, a pessoa melhora a circulação e previne o inchaço das pernas</p>

    <p>Como funciona - O sistema é parecido com o da cadeira de rodas. Em pé, o usuário dirige o equipamento girando os aros, que ficam ao alcance das mãos. O Stand Table também tem uma espécie de mesa para apoiar objetos</p>

    <p>Preço - R$ 3,2 mil</p>

    <p>Fabricante - Cajumoro</p>
     
