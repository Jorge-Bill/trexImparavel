    <h2>Instituto Novo Ser</h2>
		  <p>É uma instituição sem fins lucrativos cuja missão é a luta pelo respeito e a valorização da Cidadania das pessoas com deficiência, de forma a promover os seus direitos, a equalização das oportunidades e a superação dos obstáculos sociais predominantes ao processo de Inclusão.
Entende que é de grande importância a desconstrução da ideia e da imagem de passividade que o senso comum atribui à pessoa com deficiência. É imprescindível concebê-la como participativa, integrada ao meio social, fortalecendo sua adaptação, potencializando aptidões, ampliando seu campo de oportunidades e colocando-a em igualdade de condições. É fundamental, portanto, reconhecer a ideia de que, se por um lado há limitações em sua condição física, por outro lado sua capacidade, talento e personalidade se encontram preservados e cada vez mais fortalecidos.
A intenção é, pois, contribuir com a produção de mudanças capazes de transformar a ótica predominante sobre a deficiência.</p>
		  
		  <p><b>Site:</b><a href=" http://www.novoser.org.br/instit_quem.htm"> http://www.novoser.org.br/instit_quem.htm</a></p>
		  
		   <h2>Vidas</h2>
		   <p>A VIDAS, fundada em março de 2010, é uma associação sem fins lucrativos, com titularidade de OSCIP.
A VIDAS foi criada para proporcionar a criança e adolescente com deficiência física, oportunidades de socialização e desenvolvimento, por meio da prática de atividade física, esportiva, de convivência e de lazer, visando formar cidadãos independentes e integrados.
</p>

         <p><b>Site:</b><a href="http://www.vidas.org.br/blog/?page_id=227"> http://www.vidas.org.br/blog/?page_id=227</a></p>

 <h2>ACDEM –  Associação da Casa dos Deficientes de Ermelino Matarazzo</h2>
 <p>A ACDEM tem o compromisso de promover uma vida digna os deficientes e uma melhor qualidade de vida aos seus familiares, contribuindo com o desenvolvimento local e social da região leste de São Paulo. Desenvolve seus projetos com uma equipe qualificada com responsabilidade social a 565 deficientes mentais, 22 crianças e adolescentes no abrigo, 120 crianças e adolescentes no Centro para Criança e Adolescentes, 140 crianças na educação infantil, 2.400 pessoas mês no TELECENTRO e 1.000 famílias no Serviço de Assistência Social a Família e Proteção Básica, com 100% de gratuidade, garantindo diariamente a 1.500 pessoas, pelo menos uma refeição nutritiva.</p>
 	
	 <p>Objetivos estratégicos:</p>
	 
	<p><ul>
        <li>Garantir e assegurar, acolhimento provisório e excepcional para crianças e adolescentes de ambos os sexos, inclusive crianças e adolescentes com deficiência, em situação de medida de proteção e em situação de risco pessoal, social e de proteção;</li>
	<li>Promover o convívio, o desenvolvimento das competências pessoais, relacionais, cognitivas e produtivas da pessoa com deficiência, a autonomia, a independência, e a defesa dos direitos com a busca constante da sua inclusão na rede de serviços socioassistenciais;</li>
	<li>Habilitar e Reabilitar crianças e adolescentes de 0 a 18 anos por meio da intervenção terapêutica, proporcionando melhora na qualidade de vida e maior bem estar biopsicossocial à população envolvida, favorecendo assim sua inclusão na sociedade.</li>
	<li>Desenvolver programas e projetos que contribuam para a inclusão social e o combate a fome, por meio de programas e projetos.</li>
    </ul></p>
