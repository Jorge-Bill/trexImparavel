    <h1>Tecnologia em serviço da Acessibilidade</h1>
       <p>Pessoas com algum tipo de deficiência, física ou mental, tem dificuldades no dia a dia para realizarem algum tipo de tarefa, que envolvam sentidos, assim a tecnologia, trabalha para projetar objetos que possam ser usados mesmo que a pessoa tenha alguma deficiência ou projetado para pessoas com deficiência. Assim incluindo todos digitalmente.</p>
	   <p>No Brasil, atualmente, há 25 milhões de brasileiros com algum tipo de deficiência, segundo o ministério da saúde, ou seja 14% da população brasileira, sendo desses 40% deficientes visuais.</p>
	   <p>Assim o governo brasileiro resolveu tomar providencias para melhoria de vida, comodidade e segurança para pessoas com deficiência. Facilitado o transporte com alguns ônibus adaptados, abrindo portas no mercado de trabalho com o artigo 93º de 1991 que assegura que empresas com 100 ou mais funcionários, tenham 2% a 5% dos funcionários deficientes ou reabilitados. E para que as empresas tenham incentivo para produzir produtos com tecnologias acessíveis aos deficientes, o governo isenta-os de impostos para que os produtos cheguem mais barato nas mãos dos consumidores.</p>
    <h3>Algumas tecnologias desenvolvidas para pessoas com deficiência</h3>
        <div class="row">
            <h4 >Tactility Toshiba</h4>
            <p class="col-md-6 col-sm-6">Algumas empresas se preocuparam em simplificar o manuseio dos seus produtos em prol dos deficientes visuais. A Toshiba lançou  o celular Tactility. O design do teclado foi projetado em alto relevo, com algarismos na linguagem Braille, e permite que o usuário amarre um cordão para usar o celular em volta do pescoço.</p>
            <img class="col-md-4 col-sm-4 img-responsive thumbnail" src="imagens/celtoshiba.jpg" alt="celular Tactility. O design do teclado foi projetado em alto relevo, com algarismos na linguagem Braille">   
        </div>
        <div class="row">
            <h4 >Touch Messenger</h4>
            <p class="col-md-6 col-sm-6">A Samsung já está desenvolvendo seu segundo modelo de celulares para
cegos, ainda em fase de desenvolvimento. O primeiro, lançado em 2006, chegou a receber o prêmio no Industrial Design Excellence Awards (IDEA) no mesmo ano. O celular Touch Messenger possui uma tela que possibilita o usuário a escrever e receber mensagens de texto em Braille através dos12 botões dispostos no teclado.</p>
            <img class="col-md-4 col-sm-4 img-responsive thumbnail" src="imagens/celsamsung.jpg" alt="Celular adaptado para deficiêntes visuais, chamado Touch Menseger é branco contém 12 botões e embaixo uma pequena tela onde serão mostradas as mensagens em braille"> 
            </div>
            <div class="row">
            <h4 >TEK</h4>
                <p class="col-md-6 col-sm-6">Com o chamado dispositivo de mobilidade robótica TEK, o cadeirante consegue também ficar de pé em uma postura correta, alcançando objetos que antes, na cadeira de rodas, não seria capaz. As dimensões reduzidas do dispositivo em relação às da cadeira de rodas favorecem a acessibilidade.</p>
                <img class="col-md-4 col-sm-4 img-responsive thumbnail" src="imagens/tek.jpg" alt="uma imagem ilustrando um cadeirante usando tek para ficar em pé cozinhando e em um super mercado pegando itens que estão no alto"> 
            </div>
            <div class="row">
                <h4 >HAL</h4>
                    <p class="col-md-6 col-sm-6">A criatividade dos japoneses e sua aptidão para robótica também estão a serviço da mobilidade. É o que vemos no exoesqueleto HAL (Hybrid Assistive Limb, ou membro de assistência híbrida), um traje robótico capaz de sustentar o peso da pessoa e ajudá-la a se locomover. Ele permite que pessoas com paralisia ou com lesões na medula espinhal possam ficar de pé, andar, subir escadas e até carregar objetos pesados.</p>
                    <img class="col-md-4 col-sm-4 img-responsive thumbnail" src="imagens/hal.jpg" alt="uma imagem ilustrando um cadeirante usando tek para ficar em pé cozinhando e em um super mercado pegando itens que estão no alto"> 
              </div>
