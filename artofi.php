    <h2><a id="conteudo"><img src="imagens/MiniLogoV1_50.png" alt="logo de um tiranosauro rex verde usando extensores vermelhos e amarelos para seus pequenos braços"> Oficina de Inclusão</a></h2>
    <hr/>
    <img src="imagens/apae.jpg" alt="Duas mão segurando uma flor, é o logo da instituição APAE" class="pull-right">
			<p>Dentro da perspectiva de formação, qualificação, profissionalização e de inserção no mundo do trabalho, a APAE de Bebedouro desenvolve o Programa Oficina, que é integrado por três projetos, a saber: Projeto Fazendo Arte, Projeto Trampolim e o Programa de Educação para o Trabalho Trampolim.

<p>O Projeto Fazendo Arte é um espaço de educação para o mundo do trabalho, de formação e qualificação profissional, de desenvolvimento da cidadania e de pessoas que buscam, antes de qualquer coisa, canais de promoção humana que assegurem possibilidades de expressão e de criação para todos, independentemente da condição física, sensorial, social e econômica.</p>

<p>No âmbito deste projeto são desenvolvidas ações que objetivam formar, qualificar e profissionalizar pessoas com deficiência intelectual e múltipla, seguindo o processo de contratação regular, nos termos da legislação trabalhista e previdenciária ou segundo a modalidade de promoção do trabalho por conta própria, ou seja, trabalho autônomo, cooperativado ou em regime de economia familiar.</p>

<p>Para que esta qualificação ou profissionalização aconteça são disponibilizados aos alunos oficina de formação básica, com treinamentos que contemplem a aquisição de conhecimentos básicos em português e matemática, oficina de formação complementar, com treinamentos que contemplem o estudo de comportamentos adequados ao mundo do trabalho, aspectos de higiene pessoal e relações interpessoais, oficina de formação cidadã com treinamentos voltados as áreas relacionadas aos direitos e deveres do cidadão e do trabalhador e ao exercício da cidadania e oficinas profissionalizantes.</p>

<p>Nas oficinas profissionalizantes são oferecidos aos alunos treinamentos em técnicas de marcenaria, de tecelagem, de costura e bordado, de fabricação de papel artesanal, de desenvolvimento e fabricação de produtos utilizando resíduos descartados, em especial papel, plástico, tecidos, dentre outros. Nas oficinas profissionalizantes os alunos conseguem, por meio dos treinamentos e cursos de qualificação, desenvolver e fortalecer as habilidades e competências motoras, sensoriais e cognitivas que facilitam a aquisição de competências sociais para a inclusão efetiva e permanente na sociedade.</p>

<p>Após o cumprimento das etapas de formação e qualificação do Projeto Fazendo Arte, os alunos são incluídos no Projeto Trampolim, cujo objetivo principal é promover a educação profissional e a inclusão dos alunos no mundo do trabalho, quer em empresas do mercado local ou em grupos de produção criados no âmbito do Programa Oficina.</p>

<p>Nessa perspectiva a parceria com o SENAC resultou no desenvolvimento do Programa de Educação para o Trabalho - PET, surgindo assim o PET – Trampolim, que na primeira edição, em 2009, preparou 27 alunos da APAE para o mundo do trabalho e todos os alunos eram da APAE de Bebedouro e na segunda edição em 2010, a turma foi composta por alunos da APAE de Bebedouro, Pitangueiras e por demanda espontânea da comunidade.</p>

